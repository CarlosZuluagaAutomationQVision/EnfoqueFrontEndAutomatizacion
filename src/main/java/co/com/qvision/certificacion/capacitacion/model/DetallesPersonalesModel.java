package co.com.qvision.certificacion.capacitacion.model;

public class DetallesPersonalesModel extends DatosInicioSesionModel{

    private String nombre;
    private String apellido;
    private String idEmpleado;
    private String numeroLicencia;
    private String fechaExpiracionLicencia;
    private String ssnNumero;
    private String genero;
    private String estadoCivil;
    private String nacionalidad;
    private String fechaNacimiento;
    private String apodo;
    private String servicioMilitar;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getIdEmpleado() {
        return idEmpleado;
    }

    public void setIdEmpleado(String idEmpleado) {
        this.idEmpleado = idEmpleado;
    }

    public String getNumeroLicencia() {
        return numeroLicencia;
    }

    public void setNumeroLicencia(String numeroLicencia) {
        this.numeroLicencia = numeroLicencia;
    }

    public String getFechaExpiracionLicencia() {
        return fechaExpiracionLicencia;
    }

    public void setFechaExpiracionLicencia(String fechaExpiracionLicencia) {
        this.fechaExpiracionLicencia = fechaExpiracionLicencia;
    }

    public String getSsnNumero() {
        return ssnNumero;
    }

    public void setSsnNumero(String ssnNumero) {
        this.ssnNumero = ssnNumero;
    }

    public String getGenero() {
        return genero;
    }

    public void setGenero(String genero) {
        this.genero = genero;
    }

    public String getEstadoCivil() {
        return estadoCivil;
    }

    public void setEstadoCivil(String estadoCivil) {
        this.estadoCivil = estadoCivil;
    }

    public String getNacionalidad() {
        return nacionalidad;
    }

    public void setNacionalidad(String nacionalidad) {
        this.nacionalidad = nacionalidad;
    }

    public String getFechaNacimiento() {
        return fechaNacimiento;
    }

    public void setFechaNacimiento(String fechaNacimiento) {
        this.fechaNacimiento = fechaNacimiento;
    }

    public String getApodo() {
        return apodo;
    }

    public void setApodo(String apodo) {
        this.apodo = apodo;
    }

    public String getServicioMilitar() {
        return servicioMilitar;
    }

    public void setServicioMilitar(String servicioMilitar) {
        this.servicioMilitar = servicioMilitar;
    }


}
